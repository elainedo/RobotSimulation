#include "obstacle.h"
#include "color.h"

/*******************************************************************************
 * Static Variables
 ******************************************************************************/
uint Obstacle::next_id_ = 0;

/*******************************************************************************
 * Constructors/Destructor
 ******************************************************************************/
Obstacle::Obstacle(double radius, const Position& pos,
                                   const Color& color) :
    ArenaImmobileEntity(radius, pos, color),
    id_(-1) {
  id_ = next_id_++;
}